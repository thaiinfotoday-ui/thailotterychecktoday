export type Language = keyof typeof translations;
export type TranslationKeys = typeof translations.en;

// Helper function to get translations
export function getTranslations(lang: Language = 'th'): TranslationKeys {
    return (translations[lang] || translations.th) as TranslationKeys;
}

// Helper function to get specific translation
export function t(lang: Language, key: string): string {
    const keys = key.split('.');
    let value: any = translations[lang] || translations.th;

    for (const k of keys) {
        value = value[k];
        if (value === undefined) break;
    }

    return value || key;
}
